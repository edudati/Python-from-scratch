# Describes diferents types of variables

x = input('type something: ')
print(type(x))
print('Is numeric?', x.isnumeric())
print('Is only space?', x.isspace())
print('Is only letter?', x.isalpha())
print('Is alphanumeric?', x.isalnum())
print('Is UPPER?', x.isupper())
print('Is lower?', x.islower())
print('Is capitalized?', x.istitle())

